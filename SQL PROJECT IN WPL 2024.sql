--5) INNINGS TABLES

CREATE TABLE INNINGS_DETAILS (
                   INNINGS_ID NUMBER,
                   MATCH_ID NUMBER,
                   TEAM1_ID NUMBER,
                   TEAM1_RUNS NUMBER,
                   TEAM1_WICKETS NUMBER,
                   TEAM1_OVER NUMBER,
