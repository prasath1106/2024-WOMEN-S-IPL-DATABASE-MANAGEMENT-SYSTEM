--1)TEAMS TABLE

CREATE TABLE WPL_TEAMS_2024 (
                   TEAM_ID NUMBER ,
                   TEAM_NAME VARCHAR2(50) NOT NULL,
                   HEAD_COACH VARCHAR2(50),
                   CAPTAIN VARCHAR2(50),
                   TEAM_OWNER VARCHAR2(100),
                   TEAM_CITY VARCHAR2(20),
    
          CONSTRAINTS WPL_TEAMS_2024PK PRIMARY KEY (TEAM_ID) 

      ); 

      
--2)PLAYERS TABLE

CREATE TABLE PLAYER_DETAILS (
                       PLAYER_ID NUMBER,
                       PLAYER_NAME VARCHAR2(20)NOT NULL,
                       PLAYER_BORN DATE,
                       WPL_DEBUT NUMBER,
                       TEAM_ID NUMBER,
                       PLAYER_AGE NUMBER,
                       BATTING_STYLE VARCHAR2(50),
                       BOWLING_STYLE VARCHAR2(50),
                       PLAYER_NATIONALITY VARCHAR2(50),
                       PLAYER_ROLE VARCHAR2(20),
            
           CONSTRAINTS PLAYER_DETAILS_PK PRIMARY KEY (PLAYER_ID),
           CONSTRAINTS PLAYER_DETAILS_FK FOREIGN KEY (TEAM_ID) REFERENCES WPL_TEAMS_2024(TEAM_ID)ON DELETE CASCADE
    );
ALTER TABLE PLAYER_DETAILS
MODIFY PLAYER_ROLE VARCHAR2(50);
