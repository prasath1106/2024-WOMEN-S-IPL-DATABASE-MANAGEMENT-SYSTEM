        CONSTRAINTS INNINGS_DETAILS_FK1 FOREIGN KEY (TEAM1_ID) REFERENCES WPL_TEAMS_2024(TEAM_ID),
        CONSTRAINTS INNINGS_DETAILS_FK2 FOREIGN KEY (TEAM2_ID) REFERENCES WPL_TEAMS_2024(TEAM_ID)ON DELETE CASCADE
