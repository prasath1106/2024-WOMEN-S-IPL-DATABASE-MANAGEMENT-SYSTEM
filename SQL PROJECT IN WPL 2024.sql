--6)PLAYER MATCH TABLE


CREATE TABLE PLAYERS_BATTING (
                              INNINGS_ID NUMBER,
                              MATCH_ID NUMBER,
                              TEAM_ID NUMBER,
                              PLAYER_ID NUMBER,
                              RUNS NUMBER,
                              BALLS NUMBER,
                              FOURS NUMBER,
                              SIXES NUMBER,
                              STRIKE_RATE NUMBER,
                              
            CONSTRAINTS PLAYERS_BATTING_FK FOREIGN KEY (TEAM_ID) REFERENCES WPL_TEAMS_2024(TEAM_ID),                  
            CONSTRAINTS PLAYERS_BATTING_FK1 FOREIGN KEY (PLAYER_ID) REFERENCES PLAYER_DETAILS(PLAYER_ID),
            CONSTRAINTS PLAYERS_BATTING_FK2 FOREIGN KEY (MATCH_ID) REFERENCES MATCHES_DETAILS(MATCH_ID),
            CONSTRAINTS PLAYERS_BATTING_FK3 FOREIGN KEY (INNINGS_ID) REFERENCES INNINGS_DETAILS(INNINGS_ID) ON DELETE CASCADE
