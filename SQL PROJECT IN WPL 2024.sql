--3) PLAYER OVERVIEW

CREATE TABLE PLAYER_OVERVIEW(
                              PLAYER_ID NUMBER,
                              TEAM_ID NUMBER,
                              PLAYER_NAME VARCHAR2(100),
                              TOTAL_MATCHES NUMBER,
                              RUNS NUMBER,
                              WICKETS NUMBER,
                CONSTRAINTS PLAYER_OVERVIEW_PK PRIMARY KEY (PLAYER_ID),           
                CONSTRAINTS PLAYER_OVERVIEW_FK FOREIGN KEY (PLAYER_ID) REFERENCES PLAYER_DETAILS(PLAYER_ID),
                CONSTRAINTS PLAYER_OVERVIEW_FK1 FOREIGN KEY (TEAM_ID) REFERENCES WPL_TEAMS_2024(TEAM_ID)ON DELETE CASCADE 
        );
                

--4) MATCHES TABLE


